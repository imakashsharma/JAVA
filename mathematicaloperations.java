package akashsharma;
import java.util.*;

public class mathematicaloperations {
	public static void main(String[] args) {
	int c=-5 + 8 * 6;
	int d=(55+9) % 9;
	int e=20 + -3*5 / 8;
	int f=5 + 15 / 3 * 2 - 8 % 3;
	System.out.println("-5 + 8 * 6 results :" + c);
	System.out.println("(55+9) % 9 results :" + d);
	System.out.println("20 + -3*5 / 8 results :" + e);
	System.out.println("5 + 15 / 3 * 2 - 8 % 3 results :" + f);

}
}